package com.atl.atlmovil.entidades;

public class Producto {
	private int codigoProducto;
	private String descripcionProducto;
	private String colorProducto;
	private String sexoProducto;
	private String materialProducto;
	private String calidadProducto;
	private double precioProducto;
	public int getCodigoProducto() {
		return codigoProducto;
	}
	public void setCodigoProducto(int codigoProducto) {
		this.codigoProducto = codigoProducto;
	}
	public String getDescripcionProducto() {
		return descripcionProducto;
	}
	public void setDescripcionProducto(String descripcionProducto) {
		this.descripcionProducto = descripcionProducto;
	}
	public String getColorProducto() {
		return colorProducto;
	}
	public void setColorProducto(String colorProducto) {
		this.colorProducto = colorProducto;
	}
	public String getSexoProducto() {
		return sexoProducto;
	}
	public void setSexoProducto(String sexoProducto) {
		this.sexoProducto = sexoProducto;
	}
	public String getMaterialProducto() {
		return materialProducto;
	}
	public void setMaterialProducto(String materialProducto) {
		this.materialProducto = materialProducto;
	}
	public String getCalidadProducto() {
		return calidadProducto;
	}
	public void setCalidadProducto(String calidadProducto) {
		this.calidadProducto = calidadProducto;
	}
	public double getPrecioProducto() {
		return precioProducto;
	}
	public void setPrecioProducto(double precioProducto) {
		this.precioProducto = precioProducto;
	}
	
}
