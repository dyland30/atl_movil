package com.atl.atlmovil.entidades;

public class Grupo {
private long codigoGrupo;
private String nombreGrupo;
private String titularGrupo;
public long getCodigoGrupo() {
	return codigoGrupo;
}
public void setCodigoGrupo(long codigoGrupo) {
	this.codigoGrupo = codigoGrupo;
}
public String getNombreGrupo() {
	return nombreGrupo;
}
public void setNombreGrupo(String nombreGrupo) {
	this.nombreGrupo = nombreGrupo;
}
public String getTitularGrupo() {
	return titularGrupo;
}
public void setTitularGrupo(String titularGrupo) {
	this.titularGrupo = titularGrupo;
}

}
