package com.atl.atlmovil.entidades;

public class EmpresaCarga {
	private int codigoEmpresaCarga;
	private String nombreEmpresaCarga;
	private String direccionEmpresaCarga;
	private String horarioEmpresaCarga;
	private String rucEmpresaCarga;
	public int getCodigoEmpresaCarga() {
		return codigoEmpresaCarga;
	}
	public void setCodigoEmpresaCarga(int codigoEmpresaCarga) {
		this.codigoEmpresaCarga = codigoEmpresaCarga;
	}
	public String getNombreEmpresaCarga() {
		return nombreEmpresaCarga;
	}
	public void setNombreEmpresaCarga(String nombreEmpresaCarga) {
		this.nombreEmpresaCarga = nombreEmpresaCarga;
	}
	public String getDireccionEmpresaCarga() {
		return direccionEmpresaCarga;
	}
	public void setDireccionEmpresaCarga(String direccionEmpresaCarga) {
		this.direccionEmpresaCarga = direccionEmpresaCarga;
	}
	public String getHorarioEmpresaCarga() {
		return horarioEmpresaCarga;
	}
	public void setHorarioEmpresaCarga(String horarioEmpresaCarga) {
		this.horarioEmpresaCarga = horarioEmpresaCarga;
	}
	public String getRucEmpresaCarga() {
		return rucEmpresaCarga;
	}
	public void setRucEmpresaCarga(String rucEmpresaCarga) {
		this.rucEmpresaCarga = rucEmpresaCarga;
	}
}
