package com.atl.atlmovil.entidades;

public class Cliente {
private long codigoCliente;
private String representanteCliente;
private String direccionEntregaCliente;
private String celularCliente;
private long codigoGrupo;
private long codigoPersona;
private long codigoEmpleado;
public long getCodigoCliente() {
	return codigoCliente;
}
public void setCodigoCliente(long codigoCliente) {
	this.codigoCliente = codigoCliente;
}
public String getRepresentanteCliente() {
	return representanteCliente;
}
public void setRepresentanteCliente(String representanteCliente) {
	this.representanteCliente = representanteCliente;
}
public String getDireccionEntregaCliente() {
	return direccionEntregaCliente;
}
public void setDireccionEntregaCliente(String direccionEntregaCliente) {
	this.direccionEntregaCliente = direccionEntregaCliente;
}
public String getCelularCliente() {
	return celularCliente;
}
public void setCelularCliente(String celularCliente) {
	this.celularCliente = celularCliente;
}
public long getCodigoGrupo() {
	return codigoGrupo;
}
public void setCodigoGrupo(long codigoGrupo) {
	this.codigoGrupo = codigoGrupo;
}
public long getCodigoPersona() {
	return codigoPersona;
}
public void setCodigoPersona(long codigoPersona) {
	this.codigoPersona = codigoPersona;
}
public long getCodigoEmpleado() {
	return codigoEmpleado;
}
public void setCodigoEmpleado(long codigoEmpleado) {
	this.codigoEmpleado = codigoEmpleado;
}

}
