package com.atl.atlmovil.entidades;

public class Talla {
private long codigoProducto;
private int numeroTalla;
private int stockDisponibleTalla;
public long getCodigoProducto() {
	return codigoProducto;
}
public void setCodigoProducto(long codigoProducto) {
	this.codigoProducto = codigoProducto;
}
public int getNumeroTalla() {
	return numeroTalla;
}
public void setNumeroTalla(int numeroTalla) {
	this.numeroTalla = numeroTalla;
}
public int getStockDisponibleTalla() {
	return stockDisponibleTalla;
}
public void setStockDisponibleTalla(int stockDisponibleTalla) {
	this.stockDisponibleTalla = stockDisponibleTalla;
}
}
